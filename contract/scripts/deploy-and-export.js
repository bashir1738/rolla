/**
 * Deploy Rolla contracts via Ignition and export addresses to mobile/constants/addresses.ts
 *
 * Usage:
 *   npx hardhat run scripts/deploy-and-export.js --network sepolia
 */
const hre = require("hardhat");
const fs = require("fs");
const path = require("path");

async function main() {
  const { ethers, ignition } = hre;

  console.log(`\nDeploying Rolla to ${hre.network.name}...\n`);

  const RollaModule = require("../ignition/modules/RollaModule");
  const { ajoCircle, rollaVault } = await ignition.deploy(RollaModule);

  const ajoAddress   = await ajoCircle.getAddress();
  const vaultAddress = await rollaVault.getAddress();

  console.log(`AjoCircle  deployed → ${ajoAddress}`);
  console.log(`RollaVault deployed → ${vaultAddress}`);

  // ── Etherscan verification ──────────────────────────────────────────────
  if (hre.network.name !== "hardhat" && hre.network.name !== "localhost") {
    console.log("\nWaiting 5 blocks before verifying...");
    const provider = ethers.provider;
    const startBlock = await provider.getBlockNumber();
    while ((await provider.getBlockNumber()) < startBlock + 5) {
      await new Promise((r) => setTimeout(r, 3000));
    }

    const SEPOLIA = {
      USDT:           "0x7169D38820dfd117C3FA1f22a697dBA58d90BA06",
      UNISWAP_ROUTER: "0x3bFA4769FB09eefC5a80d6E87c3B9C650f7Ae48",
      AAVE_POOL:      "0x6Ae43d3271ff6888e7Fc43Fd7321a503ff738951",
      A_USDT:         "0xaA8E23Fb1079EA71e0a56F48a2aA51851D8433D0",
      WETH:           "0xfFf9976782d46CC05630D1f6eBAb18b2324d6B14",
    };

    try {
      await hre.run("verify:verify", {
        address: ajoAddress,
        constructorArguments: [SEPOLIA.USDT, SEPOLIA.UNISWAP_ROUTER, SEPOLIA.WETH],
      });
      console.log("AjoCircle verified ✓");
    } catch (e) {
      console.warn("AjoCircle verification failed:", e.message);
    }

    try {
      await hre.run("verify:verify", {
        address: vaultAddress,
        constructorArguments: [
          SEPOLIA.USDT,
          SEPOLIA.UNISWAP_ROUTER,
          SEPOLIA.AAVE_POOL,
          SEPOLIA.A_USDT,
          SEPOLIA.WETH,
        ],
      });
      console.log("RollaVault verified ✓");
    } catch (e) {
      console.warn("RollaVault verification failed:", e.message);
    }
  }

  // ── Export addresses to mobile ──────────────────────────────────────────
  const mobileConstantsDir = path.resolve(__dirname, "../../mobile/constants");
  fs.mkdirSync(mobileConstantsDir, { recursive: true });

  const addressesContent = `// Auto-generated by deploy-and-export.js — do not edit manually
// Network: ${hre.network.name} | ${new Date().toISOString()}

export const CONTRACT_ADDRESSES = {
  AJO_CIRCLE:  "${ajoAddress}" as \`0x\${string}\`,
  ROLLA_VAULT: "${vaultAddress}" as \`0x\${string}\`,
} as const;

export const TOKEN_ADDRESSES = {
  USDT: "0x7169D38820dfd117C3FA1f22a697dBA58d90BA06" as \`0x\${string}\`,
  WETH: "0xfFf9976782d46CC05630D1f6eBAb18b2324d6B14" as \`0x\${string}\`,
} as const;

export const PROTOCOL_ADDRESSES = {
  UNISWAP_ROUTER: "0x3bFA4769FB09eefC5a80d6E87c3B9C650f7Ae48" as \`0x\${string}\`,
  UNISWAP_QUOTER: "0xEd1f6473345F45b75833fd55D5ADbEb76d79229" as \`0x\${string}\`,
  AAVE_POOL:      "0x6Ae43d3271ff6888e7Fc43Fd7321a503ff738951" as \`0x\${string}\`,
  CHAINLINK_ETH:  "0x694AA1769357215DE4FAC081bf1f309aDC325306" as \`0x\${string}\`,
} as const;
`;

  const outPath = path.join(mobileConstantsDir, "addresses.ts");
  fs.writeFileSync(outPath, addressesContent);
  console.log(`\nAddresses exported → ${outPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
